  static Tour fromFirestore(Map<String, dynamic> data, String id) {
    return Tour(
      id: id,
      title: data['title'] ?? '',
      hotelName: data['hotelName'] ?? '',
      hotelId: data['hotelId'] ?? id,
      country: data['country'] ?? '',
      city: data['city'] ?? '',
      description: data['description'] ?? '',
      imageUrl: data['imageUrl'] ?? '',
      imageUrls: List<String>.from(data['imageUrls'] ?? []),
      price: (data['price'] ?? 0).toDouble(),
      originalPrice: (data['originalPrice'] ?? 0).toDouble(),
      rating: (data['rating'] ?? 0).toDouble(),
      reviewsCount: data['reviewsCount'] ?? 0,
      stars: data['stars'] ?? 0,
      nights: data['nights'] ?? 0,
      mealType: data['mealType'] ?? '',
      departureCity: data['departureCity'] ?? 'Алматы',
      departureDate: data['departureDate'] != null
          ? DateTime.parse(data['departureDate'])
          : DateTime.now().add(const Duration(days: 14)),
      flightInfo: data['flightInfo'] ?? '',
      isHot: data['isHot'] ?? false,
      availableSeats: data['availableSeats'] ?? 10,
      included: List<String>.from(data['included'] ?? []),
      notIncluded: List<String>.from(data['notIncluded'] ?? []),
    );
  }

  static Map<String, dynamic> toFirestore(Tour t) {
    return {
      'title': t.title,
      'hotelName': t.hotelName,
      'hotelId': t.hotelId,
      'country': t.country,
      'city': t.city,
      'description': t.description,
      'imageUrl': t.imageUrl,
      'imageUrls': t.imageUrls,
      'price': t.price,
      'originalPrice': t.originalPrice,
      'basePrice': t.originalPrice,
      'rating': t.rating,
      'reviewsCount': t.reviewsCount,
      'stars': t.stars,
      'nights': t.nights,
      'mealType': t.mealType,
      'departureCity': t.departureCity,
      'departureDate': t.departureDate.toIso8601String(),
      'flightInfo': t.flightInfo,
      'isHot': t.isHot,
      'availableSeats': t.availableSeats,
      'included': t.included,
      'notIncluded': t.notIncluded,
    };
  }

