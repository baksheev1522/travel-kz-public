class HotelRoomVariant {
  final String id;
  final String roomType;
  final String roomSize;
  final String mealType;
  final double price;
  final double pricePerMonth;
  final bool isBestPrice;
  final String imageUrl;

  const HotelRoomVariant({
    required this.id,
    required this.roomType,
    required this.roomSize,
    required this.mealType,
    required this.price,
    required this.pricePerMonth,
    this.isBestPrice = false,
    required this.imageUrl,
  });

  factory HotelRoomVariant.fromMap(Map<String, dynamic> data) {
    return HotelRoomVariant(
      id: data['id'] ?? '',
      roomType: data['roomType'] ?? '',
      roomSize: data['roomSize'] ?? '',
      mealType: data['mealType'] ?? '',
      price: (data['price'] ?? 0).toDouble(),
      pricePerMonth: (data['pricePerMonth'] ?? 0).toDouble(),
      isBestPrice: data['isBestPrice'] ?? false,
      imageUrl: data['imageUrl'] ?? '',
    );
  }

  Map<String, dynamic> toMap() => {
    'id': id,
    'roomType': roomType,
    'roomSize': roomSize,
    'mealType': mealType,
    'price': price,
    'pricePerMonth': pricePerMonth,
    'isBestPrice': isBestPrice,
    'imageUrl': imageUrl,
  };
}

